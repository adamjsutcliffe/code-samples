using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using Peak.Speedoku.Scripts.Common;
using Peak.Speedoku.Scripts.Autogenerated;
using System;
using UnityEngine.UI;
using UI.Pagination;

namespace Peak.Speedoku.Scripts.ScenesLogic
{
    public class FeedbackPopupActivator : SceneActivationBehaviour<FeedbackPopupActivator>
    {
        [SerializeField] private string feedbackUrl;
        public Button closeButton;

        public override void Initialize()
        {
            base.Initialize();
        }

        public override void Show(bool animated = false)
        {
            base.Show(animated);
        }

        public override void Hide()
        {
            base.Hide();
        }

        public void GiveFeedbackButtonHandelr()
        {
            Application.OpenURL(feedbackUrl);
            SceneActivationBehaviour<GameLogicActivator>.Instance.GameController.FeedbackGivenAcccepted();
        }

        public void CloseButtonHandler()
        {
            InterfaceController.Instance.Hide(GameWindow.FeedbackPopup);
            LocalisationController.Instance.PlayAudioClip(Settings.Autogenerated.SoundSettingsKey.PopupSound);
        }
    }
}
